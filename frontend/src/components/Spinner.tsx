type SpinnerProps = {
  size?: 'sm' | 'md' | 'lg'
  className?: string
}

const SIZE_MAP: Record<NonNullable<SpinnerProps['size']>, string> = {
  sm: 'h-4 w-4 border-2',
  md: 'h-6 w-6 border-[3px]',
  lg: 'h-8 w-8 border-[3px]',
}

export const Spinner = ({ size = 'md', className = '' }: SpinnerProps) => {
  const sizeClasses = SIZE_MAP[size]

  return (
    <span
      className={`inline-flex animate-spin rounded-full border-slate-300 border-t-primary ${sizeClasses} ${className}`.trim()}
      aria-hidden="true"
    />
  )
}
